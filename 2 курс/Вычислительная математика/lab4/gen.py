# Название тестового файла
filename = "input1.txt"

# Исходные данные по вашему варианту (11 точек)
test_data = [
    (0.0, 0.0),
    (0.2, 0.089),
    (0.4, 0.177),
    (0.6, 0.264),
    (0.8, 0.339),
    (1.0, 0.400),
    (1.2, 0.434),
    (1.4, 0.436),
    (1.6, 0.411),
    (1.8, 0.369),
    (2.0, 0.320)
]

try:
    with open(filename, "w", encoding="utf-8") as f:
        for x, y in test_data:
            # Записываем каждую пару точек на новой строке через пробел
            f.write(f"{x:.1f} {y:.3f}\n")
    print(f"Файл '{filename}' успешно создан и заполнен 11 тестовыми точками!")
except Exception as e:
    print(f"Не удалось создать файл: {e}")
