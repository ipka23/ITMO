public class Book {
    private String title;
    private int pages;
    public Book(String name) {
        this.title = title;
    }
    public Book(int pages) {
        this.pages = pages;
    }
    public Book(String name, int pages) {
        this.title = title;
        this.pages = pages;
    }
    public String getTitle(){
        return title;
    }
}
